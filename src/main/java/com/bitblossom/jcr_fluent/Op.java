package com.bitblossom.jcr_fluent;

// EQ, - equal
// NEQ, - not-equal
// LIKE, - jcr:like
// GT, - greater-than
// LT, - less-than
// GTEQ, - greater-than or equal-to
// LTEQ, - less-than or equal-to
// ACTIV, - jcr node activated
// NACTIV - jcr node not activated

public enum Op {
  EQ, NEQ, LIKE, GT, LT, GTEQ, LTEQ, ACTIV, NACTIV
}
